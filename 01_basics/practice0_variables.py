#GOAL: Learn how variables and print work in python

# Creating a variable called age and assigning a value
age = 30

# Printing the value stored in age
print(age)

# Reassigning the variable to new value
age = 40

# Printing again the value to show the updated value
print(age)

"""
OUTPUT: 
30 
40

EXPLANATION:
- Variables are names pointing to values
- The value can change over time 

"""